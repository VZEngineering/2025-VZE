# These are the escape sequences in python
# \"
# \'
# \\
# \n (new line)

course = "Python \"Programming"
print(course)  # this will print the string

print(len(course))  # this will print the length of the string
print(course[0])  # this will print the first character of the string
print(course[-1])  # this will print the last character of the string
print(course[0:3])  # this will print the first three characters of the string
print(course[0:])  # this will print the entire string``
print(course[:3])  # this will print the first three characters of the string
print(course[:])  # this will print the entire string
