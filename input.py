"""This is a simple program to take input and understand dat types in Python"""

x = input("x: ")
y = int(x) + 1
# f-string is used to format the string utilizing {} to insert variables
print(f"x: {x}, y: {y}")

# int (x) is used to convert the string to an integer
# float (x) is used to convert the string to a float
# bool (x) is used to convert the string to a boolean
# str (x) is used to convert the string to a string

# Falsy values: "", 0, None ; in a boolean context, these values are considered False
